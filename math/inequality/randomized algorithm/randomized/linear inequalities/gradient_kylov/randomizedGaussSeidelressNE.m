function [x,iter,error_k,iter_k,index_k] = randomizedGaussSeidelressNE(A, b, x0,alpha ,maxit,tol,exactx,debug)
%% 参数设定
% 输入参数
% A, b, x0 问题的系数矩阵和右边项 初始值
% maxit,tol,exactx 最大迭代次数，容忍度，精确解
% 输出参数
% x iter 迭代最后的解, 实际迭代次数
% error 如果没有精确解就存储每一次迭代的梯度2范数
% iter_k 存计算梯度的迭代次数 为作图方便
% index_k 如果是随机算法则存储随机选择的序列
% exactx 精确值为空则将iter_k 放每次迭代的x值
% debug 调试程序
%%
[m, n] = size(A);
x = x0;
iter = 0;

%% 计算残差
r = b - A * x;
rs = r;
r(r<0) = 0;
norm_Ar = norm(A'*r);

if isempty(exactx)
    error_k = [norm_Ar];
    iter_k =[0];
else
    error_k = [norm(x-exactx)];
    iter_k =[x];
end

index_k=[0];
% 因为测试终止条件需要矩阵乘以向量 为了避免每次迭代都去检测终止条件因此周期检测
iter_test_stop = 1;



Acol=sum(A.*A,1)';

p=2;
r_orig = r;
x_orig = x;
rs_orig = rs;
normar = zeros(n,1);
for pickedj_f = 1:n
    col = A(:, pickedj_f);
    inc = alpha*( col' * r ) / Acol(pickedj_f);
    x(pickedj_f) = x(pickedj_f) + inc;
    rs = rs - inc*col;
    r = rs;
    r=(r+abs(r))/2;
    normar(pickedj_f) = norm(r);
    r= r_orig;
    x = x_orig;
    rs = rs_orig;
end
normar = norm(r_orig)-normar;
prob=(normar/sum(normar));
cumsumpro=cumsum(prob);
iter_index = 1;
for i=1:n-1
    k = ceil(maxit/n);
    pickedj_a(iter_index:iter_index+k-1)=repmat(i,1,k);
    iter_index=iter_index+k;
end
pickedj_a(iter_index:maxit)=repmat(n,1,maxit-iter_index+1);
%pickedj_i=randperm(maxit);
k=ceil(maxit/n);
for i = 1:maxit
 %   pickedj=sum(cumsumpro<rand)+1;
 mod(i,n)*k+ceil(i/n)
   pickedj=pickedj_a((mod(i,n))*k+mod(i,n));
    col = A(:, pickedj);
    colr = col' * r;
    inc = alpha*( colr ) / Acol(pickedj);
    x(pickedj) = x(pickedj) + inc;
    rs = rs - inc*col;
    normor=norm(r);
    r = rs;
    r=(r+abs(r))/2;
    
    normar(pickedj) = normor-norm(r);

    prob=(normar/sum(normar));
    cumsumpro=cumsum(prob);
    %   end
    iter = iter+1;
    % 主要记录迭代过程中的值 用来调试
    if mod(iter,iter_test_stop)==0
        if ~isempty(tol) || debug
            % 矩阵乘以向量
            Ar = A'*r;
            e = norm(Ar);
            normAr = norm(r);
            % 如果有容忍度 即使没有到达最大迭代次数也终止
            if ~isempty(tol)
                if normAr < tol  || e < tol
                    break;
                end
            end
        end
        % 用来记录迭代信息 可能会影响效率
        if debug
            if ~isempty(exactx)
                e = norm(x-exactx);
                iter_k =[iter_k x];
            else
                iter_k =[iter_k i];
            end
            error_k = [error_k,e];
            index_k = [index_k,pickedj];
        end
    end
end

end